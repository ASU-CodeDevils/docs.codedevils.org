/**
 * Adds meta data to the HTML page depending on its contents.
 *
 * @author Kevin Shelley (kevin.shelley@asu.edu)
 * @version 1.01
 */
(function () {
    const getTextFromHtml = html => {
        let div = document.createElement("div");
        div.innerHTML = html;
        let text = div.textContent || div.innerText || "";
        return text.replace(/(\r\n|\n|\r|\t)/gm,' ');
    };

    /**
     * Appends the custom footer to the bottom of the page.
     *
     * @param html The docsify-rendered html.
     * @returns {string} The custom footer appended to the rendered html.
     */
    const after = html => {
        let text = getTextFromHtml(html);
        console.log(text)
    };

    /**
     * Hook that initiates the call to the drive and creates/appends the footer.
     *
     * @param hook The hook generated by docsify.
     */
    const addFooter = hook => {
        hook.afterEach(html => after(html));
    };

    $docsify.plugins = [].concat(addFooter, $docsify.plugins);
}());

