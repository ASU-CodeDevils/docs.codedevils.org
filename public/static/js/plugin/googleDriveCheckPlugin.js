/**
 * Plugin to check the status of Google Drive. Since some networks block access to Drive, and images on the docs
 * page are likely stored there, the images won't render. This plugin adds a notice at the top of each page if
 * the Drive is inaccessible.
 *
 * @author Kevin Shelley (kevin.shelley@asu.edu)
 * @version 1.01
 */
(function () {
    const logoSource = 'https://drive.google.com/uc?id=1dfSKTkPgHFAvW8RiIFludY6hstSshDHH';
    const failedMessage = 'Failed to connect to Google Drive. The network you are on may be blocking it. Some files/images may not be shown.';

    /**
     * Sends an image request (can be any arbitrary image on the Drive but must be an image) and checks the returned
     * status. If there is an error rendering the image, then the error is logged and the drive is marked inaccessible.
     */
    const init = () => {
        if($docsify['driveAccessible'] === undefined) {
            // check connection to google drive
            let image = new Image();
            image.onerror = () => {
                console.error(failedMessage);
                $docsify['driveAccessible'] = false;
            };
            image.onload = () => {
                $docsify['driveAccessible'] = true;
            };
            image.src = logoSource;     // any logo on the drive will do
        }
    };

    /**
     * Initializes the image check and returns the content with the failed message appended to the top if the drive
     * is marked inaccessible.
     *
     * @param content The docsify-rendered markdown content.
     * @returns {string} The content with or without the failed message appended to the top.
     */
    const before = content => {
        init();
        return !$docsify.driveAccessible ? '!> ' + failedMessage + '\n\n' + content : content
    };

    /**
     * Hook that initiates the call to the Drive and updates the markdown contents.
     *
     * @param hook The hook generated by docsify.
     */
    const driveCheck = hook => {
        hook.beforeEach(content => {
            return before(content);
        });
    };

    $docsify.plugins = [].concat(driveCheck, $docsify.plugins);
}());