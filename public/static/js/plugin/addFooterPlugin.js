/**
 * Adds a footer to the bottom of each page displaying the copyright info and the date the site was last modified.
 *
 * @author Kevin Shelley (kevin.shelley@asu.edu)
 * @version 1.01
 */
(function () {
    /**
     * Make http request to current document location and grab the date it was last modified.
     */
    function init() {
        if(!$docsify.modified) {
            // hack for obtaining modified time
            const req = new XMLHttpRequest();
            req.open('GET', document.location, false);
            req.send(null);
            $docsify['modified'] = req.getResponseHeader('last-modified');
        }
    }

    /**
     * Appends the custom footer to the bottom of the page.
     *
     * @param html The docsify-rendered html.
     * @returns {string} The custom footer appended to the rendered html.
     */
    const after = html => {
        let footer = [
            `<hr/>`,
            `<small>`,
            `<div class="my-footer">`,
            `<div class="copyright"><a href="https://www.codedevils.org">CodeDevils</a> &copy; 2019. All rights reserved.</div>`,
            `<div class="modified">`,
            `Site modified: ` + $docsify['modified'],
            `</div>`,
            `</div>`,
            `</small>`
        ].join('');
        return html + footer;
    };

    /**
     * Hook that initiates the call to the drive and creates/appends the footer.
     *
     * @param hook The hook generated by docsify.
     */
    const addFooter = hook => {
        init();
        hook.afterEach(html => after(html));
    };

    $docsify.plugins = [].concat(addFooter, $docsify.plugins);
}());