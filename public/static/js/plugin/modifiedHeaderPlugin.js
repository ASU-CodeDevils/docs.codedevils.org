/**
 * Appends the last modified date to the top of every file.
 *
 * @author Kevin Shelley (kevin.shelley@asu.edu)
 * @version 1.01
 */
(function () {
    /**
     * Creates the last modified div and returns the appended content.
     *
     * @param content The docsify-rendered markdown content.
     * @returns {string} The last modified header with the content appended to the end of it.
     */
    const before = content => {
        return '<div class="modified-header">Last modified: <strong>{docsify-updated}</strong></div>\n\n' + content
    };

    /**
     * Hook that initiates the call to the drive and creates/appends the header.
     *
     * @param hook The hook generated by docsify.
     */
    const addHeader = hook => {
        hook.beforeEach(content => before(content));
    };

    $docsify.plugins = [].concat(addHeader, $docsify.plugins);
}());